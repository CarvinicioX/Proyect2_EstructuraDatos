    private Board generate() {
        Board Board = new Board();
        Component[] Labels = this.back.getComponents();
        int x, y;
        String[] Pos = new String[2];
        for (Component Label : Labels) {
            if (Label.getName().equals("back")) {
                break;
            }
            Pos[0] = Label.getName().charAt(0) + "";
            Pos[1] = Label.getName().charAt(1) + "";
            x = Integer.parseInt(Pos[0]);
            y = Integer.parseInt(Pos[1]);
            if ((ImageIcon) ((JLabel) Label).getIcon() == this.kingWhite) {
                Board.addPiece(new King(x, y, true));
            } else if ((ImageIcon) ((JLabel) Label).getIcon() == this.kingBlack) {
                Board.addPiece(new King(x, y, false));
            } else if ((ImageIcon) ((JLabel) Label).getIcon() == this.knightWhite) {
                Board.addPiece(new Knight(x, y, true));
            } else if ((ImageIcon) ((JLabel) Label).getIcon() == this.knightBlack) {
                Board.addPiece(new Knight(x, y, false));
            } else if ((ImageIcon) ((JLabel) Label).getIcon() == this.pawnWhite) {
                Board.addPiece(new Pawn(x, y, true));
            } else if ((ImageIcon) ((JLabel) Label).getIcon() == this.pawnBlack) {
                Board.addPiece(new Pawn(x, y, false));
            }
        }
        return Board;
    }

    private boolean verify(String piece) {
        Component[] Labels = this.back.getComponents();
        int Kingw = 0, Kingb = 0, Knightw = 0, Knightb = 0, Pawnw = 0, Pawnb = 0;
        ImageIcon Image;
        for (Component Label : Labels) {
            Image = (ImageIcon) ((JLabel) Label).getIcon();
            if (Image == this.kingWhite) {
                Kingw++;
            } else if (Image == this.kingBlack) {
                Kingb++;
            } else if (Image == this.knightWhite) {
                Knightw++;
            } else if (Image == this.knightBlack) {
                Knightb++;
            } else if (Image == this.pawnWhite) {
                Pawnw++;
            } else if (Image == this.pawnBlack) {
                Pawnb++;
            }
        }
        switch (piece) {
            case "KingWhite": {
                if (Kingw == 0) {
                    return true;
                } else {
                    JOptionPane.showMessageDialog(this, "Ya hay Agregado un Rey Blanco", "Error de Capa 8", JOptionPane.ERROR_MESSAGE);
                }
            }
            break;
            case "KingBlack": {
                if (Kingb == 0) {
                    return true;
                } else {
                    JOptionPane.showMessageDialog(this, "Ya hay Agregado un Rey Negro", "Error de Capa 8", JOptionPane.ERROR_MESSAGE);
                }
            }
            break;
            case "KnightWhite": {
                if (Knightw < 2) {
                    return true;
                } else {
                    JOptionPane.showMessageDialog(this, "Ya hay Agregado dos Caballos Blancos", "Error de Capa 8", JOptionPane.ERROR_MESSAGE);
                }
            }
            break;
            case "KnightBlack": {
                if (Knightb < 2) {
                    return true;
                } else {
                    JOptionPane.showMessageDialog(this, "Ya hay Agregado dos Caballos Negros", "Error de Capa 8", JOptionPane.ERROR_MESSAGE);
                }
            }
            break;
            case "PawnWhite": {
                if (Pawnw < 8) {
                    return true;
                } else {
                    JOptionPane.showMessageDialog(this, "Ya hay Agregado ocho Peones Blancos", "Error de Capa 8", JOptionPane.ERROR_MESSAGE);
                }
            }
            break;
            case "PawnBlack": {
                if (Pawnb < 8) {
                    return true;
                } else {
                    JOptionPane.showMessageDialog(this, "Ya hay Agregado ocho Peones Negros", "Error de Capa 8", JOptionPane.ERROR_MESSAGE);
                }
            }
            break;
            case "King": {
                if (Kingb == 1 && Kingw == 1) {
                    return true;
                } else {
                    JOptionPane.showMessageDialog(this, "No hay Suficientes Piezas\nAgregue otro Rey", "Error de Capa 8", JOptionPane.ERROR_MESSAGE);
                }
                break;
            }
            case "Pawn": {
                if (Pawnw > 0 || Pawnb > 0) {
                    return true;
                } else {
                    JOptionPane.showMessageDialog(this, "No hay Suficientes Piezas\nAgregue otro Pen", "Error de Capa 8", JOptionPane.ERROR_MESSAGE);
                }
                break;
            }
            case "Knight": {
                if (Knightw > 0 || Knightb > 0) {
                    return true;
                } else {
                    JOptionPane.showMessageDialog(this, "No hay Suficientes Piezas\nAgregue otro Caballo", "Error de Capa 8", JOptionPane.ERROR_MESSAGE);
                }
                break;
            }
        }
        return false;
    }

